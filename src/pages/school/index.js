import {
  connect,
  // useSelector,
  //   useDispatch,
} from "react-redux";
import React, {
  // useCallback,
  memo,
  useEffect,
  useState,
  useReducer,
  // useImperativeHandle,
  useRef,
  forwardRef,
} from "react";
import "./index.scss";
import { handleRoute } from "../../util/public";
import { withRouter } from "react-router-dom";
import SchoolList from "./schoolList";
import SchoolDetail from "./schoolDetail";
import {Loading} from '@/component/common'
//   import { Reducer, Context, initState } from "./reducer";
function School(props, ref) {
  let {
    location,
    activeTab,
    removeTab,
    tabid,contentHW:{
      height
    },schoolMsg,
    param, //param控制显示的模块
  } = props;
  const [Component, setComponent] = useState("");
  const [ID, setID] = useState("");
  const [loading,setLoading] = useState(true)
  // const [state, setDispatch] = useReducer(Reducer, initState);
  // let { component } = state;

  useEffect(() => {
    setComponent(param);
  }, [param]);
  // console.log(param,Component)
  useEffect(() => {
    if (location.pathname) {
      let Path = handleRoute(location.pathname);
      Path[0] === "schoolDetail" && Path[1] && setID(Path[1]);
      if(Path[1]){
        setLoading(false)
      }
    }
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);
  return (
    <Loading spinning={Component === "detail"&&loading} tip={'加载中...'} opacity={false}>
    <div className="School" style={{ height: height }}>
      {Component === "list" ? <SchoolList></SchoolList> : <></>}
      {Component === "detail" ? <SchoolDetail schoolMsg={schoolMsg} id={ID}></SchoolDetail> : <></>}
    </div></Loading>
  );
}

const mapStateToProps = (state) => {
  let {
    handleData: { teacherRecruitMsg },
    commonData: { roleMsg,contentHW },
  } = state;
  return { teacherRecruitMsg, roleMsg ,contentHW};
};
export default connect(mapStateToProps)(
  withRouter(memo(forwardRef(School)))
);
